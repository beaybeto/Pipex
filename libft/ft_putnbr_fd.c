#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char			ascii;
	unsigned int	nb;

	nb = n;
	if (n < 0)
	{
		write (fd, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
	{
		ascii = nb + '0';
		write (fd, &ascii, 1);
	}
}
