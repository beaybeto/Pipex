#include "libft.h"

int	ft_atoi(const char *str)
{
	int	temporal;
	int	sign;
	int	i;

	temporal = 0;
	i = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			sign = -sign;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		temporal = temporal * 10 + (str[i] - '0');
		i++;
	}
	return (sign * temporal);
}
